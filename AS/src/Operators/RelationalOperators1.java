package Operators;

public class RelationalOperators1 {

public static void main (String [] args ) {
		
		int a=40 , b=60 ;
		
		System.out.println(a>b);
		
		System.out.println(a<b);
		
		System.out.println(a>=b);
		
		System.out.println(a<=b);
		
		System.out.println(a==b);
		
		System.out.println(a!=b);
	}

}
