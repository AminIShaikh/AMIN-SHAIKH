package Operators;

public class RelationalOperators {
public static void main (String [] args ) {
		
		int a=60 , b=40 ;
		
		System.out.println(a>b);
		
		System.out.println(a<b);
		
		System.out.println(a>=b);
		
		System.out.println(a<=b);
		
		System.out.println(a==b);
		
		System.out.println(a!=b);
	}
}
