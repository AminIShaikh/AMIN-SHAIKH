package Casting;

public class Impicit {
	
	public static void main(String[] args) {
		
		byte a=99;
		System.out.println(a);
		
		short b=a;
		System.out.println(b);
		
		int c=b;
		System.out.println(c);
		
		long d=c;
		System.out.println(d);
		
		float e=d;
		System.out.println();
		
		double f=e;
		System.out.println(f);
	}

}

//Implicit casting is a type of Primitive Casting

//In Implicit casting conversion of lower data type of information into higher data type of information 
