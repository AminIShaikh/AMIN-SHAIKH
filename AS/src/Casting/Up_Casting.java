package Casting;

public class Up_Casting {
	
	public void Demo() {
		System.out.println("Demo from Super class");
		
	}
	public static void Memo() {
		System.out.println("Memo from Super class");
		
	}
	

}


//Up_Casting is a type of non-primitive 
//In up casting conversion takes place between the classes
//Up casting can be performed only after inheritance
//In Up casting properties inherited from Super class are only eligible for Up casting Operation
